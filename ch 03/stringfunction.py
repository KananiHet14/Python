# len() = This function return the length of the string

name = "kanani"
print(len(name))

print("-----------------------------")

# String.endswith() = this funciton tells wether the variable string ends with the string specified part or not.
                    #   if the string ends with the specified parth then it returns true otherwise it returns false.
print(name.endswith("i"))
print(name.endswith("n"))

print("-----------------------------")

# String.startswith() = this function tells wether the variable string starts with the string specified part or not.
                    #   if the string starts with the specified parth then it returns true otherwise it returns false.

print(name.startswith("k"))
print(name.startswith("a"))

print("-----------------------------")

# capitalize() = this function converts the first character of string to uppercase.
print(name.capitalize())


print("-----------------------------")