# dictionary is the keyvalue pair 
marks = {
    "name" : "harry",
    "physics" : 67,
    "maths" : 87,
    "chemistry" : 56
}

print(marks, type(marks))

# to access the value of the key
print(marks["name"])
print(marks["physics"])
print(marks["maths"])
print(marks["chemistry"])


# unordered, mutable, indexed, no duplicate keys